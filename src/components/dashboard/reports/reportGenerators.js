const LIME   = [200, 255, 0]
const DARK   = [22, 22, 22]
const DARK2  = [30, 30, 30]
const WHITE  = [255, 255, 255]
const GREY   = [180, 180, 180]
const LGREY  = [40, 40, 40]

// ── Shared PDF header/footer ──────────────────────────────────
function addPDFHeader(doc, title, period = '') {
  const w = doc.internal.pageSize.getWidth()
  doc.setFillColor(...DARK)
  doc.rect(0, 0, w, 22, 'F')
  doc.setFillColor(...LIME)
  doc.rect(0, 0, 4, 22, 'F')
  doc.setFont('helvetica', 'bold')
  doc.setFontSize(13)
  doc.setTextColor(...LIME)
  doc.text('CFO-X', 10, 14)
  doc.setTextColor(...WHITE)
  doc.setFontSize(11)
  doc.text(title.toUpperCase(), 36, 14)
  if (period) {
    doc.setFontSize(8)
    doc.setTextColor(...GREY)
    doc.text(`Period: ${period}`, w - 10, 14, { align: 'right' })
  }
}

function addPDFFooter(doc, businessName = 'Your Business') {
  const pageCount = doc.internal.getNumberOfPages()
  const w = doc.internal.pageSize.getWidth()
  const h = doc.internal.pageSize.getHeight()
  for (let i = 1; i <= pageCount; i++) {
    doc.setPage(i)
    doc.setFillColor(...DARK)
    doc.rect(0, h - 14, w, 14, 'F')
    doc.setFontSize(7)
    doc.setTextColor(...GREY)
    doc.text(`CONFIDENTIAL — Generated by CFO-X  |  ${businessName}  |  ${new Date().toLocaleDateString()}`, 10, h - 5)
    doc.text(`Page ${i} of ${pageCount}`, w - 10, h - 5, { align: 'right' })
    // Watermark
    doc.setTextColor(255, 255, 255, 0.04)
    doc.setFontSize(28)
    doc.setFont('helvetica', 'bold')
    doc.setTextColor(60, 60, 60)
    doc.text('CONFIDENTIAL', w / 2, h / 2, { align: 'center', angle: 45 })
  }
}

function tableStyles() {
  return {
    headStyles: { fillColor: DARK, textColor: LIME, fontStyle: 'bold', fontSize: 8 },
    bodyStyles: { fillColor: DARK2, textColor: WHITE, fontSize: 8 },
    alternateRowStyles: { fillColor: [26, 26, 26] },
    styles: { lineColor: LGREY, lineWidth: 0.2 },
  }
}

