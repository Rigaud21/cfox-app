// ── 3-Year Financial Model (Excel) ────────────────────────────
export function generate3YearModelExcel(businessName) {
  const wb = XLSX.utils.book_new()

  // Sheet 1: Assumptions
  const assumptions = [
    ['3-YEAR FINANCIAL MODEL ASSUMPTIONS'],
    [`Business: ${businessName}`],
    [`Generated: ${new Date().toLocaleDateString()}`],
    [],
    ['GROWTH ASSUMPTIONS', 'Year 1', 'Year 2', 'Year 3'],
    ['Monthly Revenue Growth Rate', '8.7%', '7.0%', '5.5%'],
    ['Gross Margin Target', '69%', '70%', '71%'],
    ['Operating Expense Growth', '4.7%', '4.0%', '3.5%'],
    ['Customer Acquisition Cost', '$180', '$160', '$140'],
    ['Average Revenue Per Customer', '$920', '$980', '$1,050'],
    [],
    ['STARTING METRICS'],
    ['Current Monthly Revenue', 55200, '', ''],
    ['Current Monthly Expenses', 22500, '', ''],
    ['Current Net Margin', '26.2%', '', ''],
    ['Cash on Hand', 47000, '', ''],
  ]
  const ws1 = XLSX.utils.aoa_to_sheet(assumptions)
  ws1['!cols'] = [{ wch: 32 }, { wch: 14 }, { wch: 14 }, { wch: 14 }]
  XLSX.utils.book_append_sheet(wb, ws1, 'Assumptions')

  // Sheet 2: P&L Projection
  const months = Array.from({ length: 36 }, (_, i) => {
    const d = new Date(); d.setMonth(d.getMonth() + i + 1)
    return d.toLocaleDateString('en-US', { month: 'short', year: 'numeric' })
  })
  const plHeader = ['Month', 'Revenue', 'COGS', 'Gross Profit', 'OpEx', 'Net Profit', 'Net Margin']
  const plRows = months.map((m, i) => {
    const yr = Math.floor(i / 12)
    const rates = [0.087, 0.07, 0.055]
    const rev = Math.round(55200 * Math.pow(1 + rates[yr], i + 1))
    const cogs = Math.round(rev * 0.31)
    const gp = rev - cogs
    const opex = Math.round(22500 * Math.pow(1.047, i + 1))
    const net = gp - opex
    return [m, rev, cogs, gp, opex, net, `${((net / rev) * 100).toFixed(1)}%`]
  })
  const ws2 = XLSX.utils.aoa_to_sheet([plHeader, ...plRows])
  ws2['!cols'] = [{ wch: 16 }, ...Array(6).fill({ wch: 14 })]
  XLSX.utils.book_append_sheet(wb, ws2, 'P&L Projection')

  // Sheet 3: Cash Flow Projection
  const cfHeader = ['Month', 'Net Income', 'Depreciation', 'CapEx', 'Debt Service', 'Net Cash Flow', 'Cash Balance']
  let cashBal = 47000
  const cfRows = months.map((m, i) => {
    const yr = Math.floor(i / 12)
    const rates = [0.087, 0.07, 0.055]
    const rev = Math.round(55200 * Math.pow(1 + rates[yr], i + 1))
    const opex = Math.round(22500 * Math.pow(1.047, i + 1))
    const net = Math.round(rev * 0.69) - opex
    const dep = 750
    const capex = i % 12 === 0 ? -5000 : 0
    const debt = -1100
    const ncf = net + dep + capex + debt
    cashBal += ncf
    return [m, net, dep, capex, debt, ncf, cashBal]
  })
  const ws3 = XLSX.utils.aoa_to_sheet([cfHeader, ...cfRows])
  ws3['!cols'] = [{ wch: 16 }, ...Array(6).fill({ wch: 14 })]
  XLSX.utils.book_append_sheet(wb, ws3, 'Cash Flow Projection')

  // Sheet 4: Unit Economics
  const ueData = [
    ['UNIT ECONOMICS'],
    [],
    ['Metric', 'Current', 'Year 1 Target', 'Year 2 Target', 'Year 3 Target'],
    ['Monthly Revenue', 55200, 147000, 290000, 475000],
    ['Active Customers', 60, 159, 296, 452],
    ['ARPU (Monthly)', 920, 925, 980, 1050],
    ['Customer Acquisition Cost', 180, 160, 140, 120],
    ['Gross Margin', '69%', '69%', '70%', '71%'],
    ['LTV (12 mo)', 11040, 11100, 11760, 12600],
    ['LTV:CAC Ratio', '61.3x', '69.4x', '84.0x', '105x'],
    ['Monthly Churn Rate', '2.1%', '1.8%', '1.5%', '1.2%'],
    ['Net Revenue Retention', '108%', '112%', '115%', '118%'],
  ]
  const ws4 = XLSX.utils.aoa_to_sheet(ueData)
  ws4['!cols'] = [{ wch: 26 }, { wch: 14 }, { wch: 14 }, { wch: 14 }, { wch: 14 }]
  XLSX.utils.book_append_sheet(wb, ws4, 'Unit Economics')

  // Sheet 5: Scenarios
  const scenHeader = ['Year', 'Scenario', 'Annual Revenue', 'Annual Net Profit', 'Cumulative Cash']
  const scenRows = []
  for (let yr = 1; yr <= 3; yr++) {
    const scenarios = [
      ['Bear', 0.04], ['Base', 0.087], ['Bull', 0.14]
    ]
    scenarios.forEach(([name, rate]) => {
      const rev = Math.round(55200 * 12 * Math.pow(1 + rate, yr))
      const profit = Math.round(rev * 0.24)
      const cash = 47000 + profit * yr
      scenRows.push([`Year ${yr}`, name, rev, profit, cash])
    })
  }
  const ws5 = XLSX.utils.aoa_to_sheet([scenHeader, ...scenRows])
  ws5['!cols'] = [{ wch: 8 }, { wch: 10 }, { wch: 16 }, { wch: 18 }, { wch: 16 }]
  XLSX.utils.book_append_sheet(wb, ws5, 'Scenarios (Bear-Base-Bull)')

  XLSX.writeFile(wb, `3-Year-Financial-Model-${businessName.replace(/\s/g, '-')}.xlsx`)
}

// ── Helpers ───────────────────────────────────────────────────
function makeXLSX(data, sheetName, filename, colWidths = []) {
  const wb = XLSX.utils.book_new()
  const ws = XLSX.utils.aoa_to_sheet(data)
  if (colWidths.length) ws['!cols'] = colWidths.map(w => ({ wch: w }))
  XLSX.utils.book_append_sheet(wb, ws, sheetName)
  XLSX.writeFile(wb, filename)
}

function downloadText(content, filename, mime) {
  const blob = new Blob([content], { type: mime })
  const url = URL.createObjectURL(blob)
  const a = document.createElement('a')
  a.href = url; a.download = filename; a.click()
  URL.revokeObjectURL(url)
}

